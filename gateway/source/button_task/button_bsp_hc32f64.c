static u8 read(data_stream_t* stream_in, u8* parameter)
{
	u8 i;
	if(stream_in->cmd < 8)
		i =  PORT_GetBit(BUTTON_PORT, uesr_pin_buff[stream_in->cmd]);

	return i;
}
